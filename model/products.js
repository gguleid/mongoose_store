// =================================================================================
// Dependencies
// =================================================================================

const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// =================================================================================
// Set up the Schema
// =================================================================================

const teaSchema = new Schema(
	{
		name: String,
        description: String,
        img: String,
        price: Number,
        qty: Number
	},
	);


// =================================================================================
// Compile our schema into a model 
// =================================================================================

const Tea = mongoose.model('Tea', teaSchema);


// =================================================================================
// Export Model 
// =================================================================================

module.exports = Tea;
